#include "../include/elf.h"

/*
 *=======================================================================================
 *CheckELF関数
 *この実行ファイルが正式なELFか判定する関数
 *=======================================================================================
 */
u8_t CheckELF(struct Elf32_info *elf){
	return (elf->e_ident[0] == 0x7f && elf->e_ident[1] == 'E' && elf->e_ident[2] == 'L' && elf->e_ident[3] == 'F')
		?
		1 : 0;
}
